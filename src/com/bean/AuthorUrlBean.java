package com.bean;

public class AuthorUrlBean {
	private int id;
	private int authorDescriptionId;
	private String url;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAuthorDescriptionId() {
		return authorDescriptionId;
	}
	public void setAuthorDescriptionId(int authorDescriptionId) {
		this.authorDescriptionId = authorDescriptionId;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
	
}
